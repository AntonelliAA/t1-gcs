public class Atracao {
    private String nome;
    private int capacidade;
    private int ingressos;
    private double faturameto;

    public Atracao(String nome, int capacidade, int ingressos, double faturameto) {
        this.nome = nome;
        this.capacidade = capacidade;
        this.ingressos = ingressos;
        this.faturameto = faturameto;
    }
}
